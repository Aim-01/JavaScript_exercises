//Решите задачу циклом:
// Улитка ползёт вверх по стене высотой 5 метров. Каждый день она проползает вверх на 
// 3 метра, а каждую ночь съезжает вниз на 2 метра. За сколько дней она доползет до вершины стены.

////////////

let height = 5;     
let day = 3;      
let night = 2;  

let distance = 0;    // сколько уже проползла
let days = 0;        // счётчик дней

while (distance < height) {
   
    distance = distance + day;
    days = days + 1;

    // Проверяем, достигла ли вершины
    if (distance >= height) {
        break;
    }

    // Ночь: улитка сползает вниз
    distance = distance - night;
}

console.log("Улитка доползёт за " + days + " дней");


